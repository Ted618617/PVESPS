    ON mart.ml_training_generation_daily (target_available, training_date);

CREATE INDEX IF NOT EXISTS ix_mltgd_location_date
    ON mart.ml_training_generation_daily (location_sk, training_date);

-- ============================================================
-- 3. meta.model_training_dataset_log
--    Step_06 資料集建置紀錄，提供輕量 MLOps 追蹤資訊。
-- ============================================================
CREATE TABLE IF NOT EXISTS meta.model_training_dataset_log (
    dataset_run_id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id                        BIGINT NOT NULL,
    dataset_name                  VARCHAR(100) NOT NULL,
    date_from                     DATE NOT NULL,
    date_to                       DATE NOT NULL,

    total_rows                    INTEGER NOT NULL DEFAULT 0,
    valid_rows                    INTEGER NOT NULL DEFAULT 0,
    invalid_rows                  INTEGER NOT NULL DEFAULT 0,
    missing_rate_pct              NUMERIC(8,2),
    target_type                   VARCHAR(20) NOT NULL DEFAULT 'simulated',

    created_at                    TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT ck_mtdl_total_rows
        CHECK (total_rows >= 0),

    CONSTRAINT ck_mtdl_valid_rows
        CHECK (valid_rows >= 0),

    CONSTRAINT ck_mtdl_invalid_rows
        CHECK (invalid_rows >= 0),

    CONSTRAINT ck_mtdl_rows_consistency
        CHECK (valid_rows + invalid_rows <= total_rows),

    CONSTRAINT ck_mtdl_missing_rate_pct
        CHECK (missing_rate_pct IS NULL OR (missing_rate_pct >= 0 AND missing_rate_pct <= 100)),

    CONSTRAINT ck_mtdl_target_type
        CHECK (target_type IN ('actual', 'simulated'))
);

COMMENT ON TABLE meta.model_training_dataset_log IS
'每次機器學習訓練資料集建置的紀錄表。';

COMMENT ON COLUMN meta.model_training_dataset_log.dataset_name IS
'通常填入 mart.ml_training_generation_daily。';

CREATE INDEX IF NOT EXISTS ix_mtdl_run_id
    ON meta.model_training_dataset_log (run_id);

CREATE INDEX IF NOT EXISTS ix_mtdl_dataset_name_created_at
    ON meta.model_training_dataset_log (dataset_name, created_at DESC);

CREATE INDEX IF NOT EXISTS ix_mtdl_date_range
    ON meta.model_training_dataset_log (date_from, date_to);

COMMIT;