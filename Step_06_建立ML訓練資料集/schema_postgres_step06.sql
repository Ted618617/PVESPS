CREATE INDEX IF NOT EXISTS ix_mltgd_site_date
    ON mart.ml_training_generation_daily (site_sk, training_date);

CREATE INDEX IF NOT EXISTS ix_mltgd_run_id
    ON mart.ml_training_generation_daily (run_id);

CREATE INDEX IF NOT EXISTS ix_mltgd_valid_training
    ON mart.ml_training_generation_daily (is_valid_for_training, training_date);

