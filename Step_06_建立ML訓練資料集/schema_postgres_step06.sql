BEGIN;

-- ============================================================
-- 0. 資料結構命名空間
-- ============================================================
-- mart：保存發電量標籤與模型訓練寬表。
CREATE SCHEMA IF NOT EXISTS mart;
-- meta：保存資料集建置紀錄與追蹤資訊。
CREATE SCHEMA IF NOT EXISTS meta;

-- ============================================================
-- 1. mart.fact_generation_actual_daily
--    監督式學習使用的每日發電量標籤資料表。
-- ============================================================
CREATE TABLE IF NOT EXISTS mart.fact_generation_actual_daily (
    fact_id                      BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id                       BIGINT NOT NULL,
    site_sk                      INTEGER NOT NULL,
    location_sk                  INTEGER,
    generation_date              DATE NOT NULL,

    actual_generation_kwh        NUMERIC(12,2) NOT NULL,
    actual_type                  VARCHAR(20) NOT NULL DEFAULT 'simulated',

    weather_adjustment_factor    NUMERIC(8,4),
    efficiency_factor            NUMERIC(8,4),
    maintenance_flag             BOOLEAN NOT NULL DEFAULT FALSE,
    source_note                  VARCHAR(200),

    loaded_at                    TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT ck_fgad_actual_generation_kwh
        CHECK (actual_generation_kwh >= 0),

    CONSTRAINT ck_fgad_actual_type
        CHECK (actual_type IN ('actual', 'simulated')),

    CONSTRAINT uq_fgad_site_date_type
        UNIQUE (site_sk, generation_date, actual_type)
);

COMMENT ON TABLE mart.fact_generation_actual_daily IS
'每日實際或模擬發電量標籤表，供機器學習訓練使用。';

COMMENT ON COLUMN mart.fact_generation_actual_daily.actual_type IS
'actual 表示實測發電量，simulated 表示規則或合成標籤。';

COMMENT ON COLUMN mart.fact_generation_actual_daily.weather_adjustment_factor IS
'依天氣條件推導的調整係數，例如雲量或降雨造成的衰減。';

COMMENT ON COLUMN mart.fact_generation_actual_daily.efficiency_factor IS
'估算設備或面板效率係數。';

