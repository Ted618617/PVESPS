BEGIN;

-- ============================================================
-- 0. Schemas
-- ============================================================
CREATE SCHEMA IF NOT EXISTS mart;
CREATE SCHEMA IF NOT EXISTS meta;

-- ============================================================
-- 1. mart.fact_generation_prediction_daily
--    Daily prediction fact table
-- ============================================================
CREATE TABLE IF NOT EXISTS mart.fact_generation_prediction_daily (
    prediction_id                  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id                         BIGINT NOT NULL,

    site_sk                        INTEGER NOT NULL,
    location_sk                    INTEGER,
    prediction_date                DATE NOT NULL,

    model_name                     VARCHAR(100) NOT NULL,
    model_version                  VARCHAR(50),
    prediction_type                VARCHAR(20) NOT NULL DEFAULT 'day_ahead',

    predicted_generation_kwh       NUMERIC(12,2) NOT NULL,
    actual_generation_kwh          NUMERIC(12,2),
    prediction_error_kwh           NUMERIC(12,2),
    abs_error_kwh                  NUMERIC(12,2),
    abs_pct_error                  NUMERIC(8,4),

    lower_bound_kwh                NUMERIC(12,2),
    upper_bound_kwh                NUMERIC(12,2),

    is_latest_prediction           BOOLEAN NOT NULL DEFAULT TRUE,
    is_actual_backfilled           BOOLEAN NOT NULL DEFAULT FALSE,

    source_table                   VARCHAR(150),
    source_date_from               DATE,
    source_date_to                 DATE,
    source_row_count               INTEGER,

    created_at                     TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    updated_at                     TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT ck_fgpd_predicted_generation_kwh
        CHECK (predicted_generation_kwh >= 0),

    CONSTRAINT ck_fgpd_actual_generation_kwh
        CHECK (actual_generation_kwh IS NULL OR actual_generation_kwh >= 0),

    CONSTRAINT ck_fgpd_prediction_type
        CHECK (prediction_type IN ('day_ahead', 'historical_backtest', 'reforecast')),

    CONSTRAINT ck_fgpd_source_row_count
        CHECK (source_row_count IS NULL OR source_row_count >= 0),

    CONSTRAINT uq_fgpd_site_date_model_version
        UNIQUE (site_sk, prediction_date, model_name, model_version, prediction_type)
);

COMMENT ON TABLE mart.fact_generation_prediction_daily IS
'Daily prediction fact table for site-level generation forecasting.';

COMMENT ON COLUMN mart.fact_generation_prediction_daily.prediction_type IS
'day_ahead = production-like daily scoring; historical_backtest = retrospective scoring; reforecast = rerun / revised prediction';

COMMENT ON COLUMN mart.fact_generation_prediction_daily.is_latest_prediction IS
'Whether this row should be considered the active/latest prediction for the site-date';

COMMENT ON COLUMN mart.fact_generation_prediction_daily.is_actual_backfilled IS
'Whether actual generation has been backfilled for evaluation';

COMMENT ON COLUMN mart.fact_generation_prediction_daily.prediction_error_kwh IS
'actual_generation_kwh - predicted_generation_kwh';

COMMENT ON COLUMN mart.fact_generation_prediction_daily.abs_pct_error IS
'Absolute percentage error, usually abs(actual-predicted)/actual * 100';

