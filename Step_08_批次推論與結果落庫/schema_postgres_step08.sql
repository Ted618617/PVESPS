CREATE INDEX IF NOT EXISTS ix_fgpd_prediction_date
    ON mart.fact_generation_prediction_daily (prediction_date);

CREATE INDEX IF NOT EXISTS ix_fgpd_site_date
    ON mart.fact_generation_prediction_daily (site_sk, prediction_date);

CREATE INDEX IF NOT EXISTS ix_fgpd_run_id
    ON mart.fact_generation_prediction_daily (run_id);

CREATE INDEX IF NOT EXISTS ix_fgpd_model_name
    ON mart.fact_generation_prediction_daily (model_name, model_version);

CREATE INDEX IF NOT EXISTS ix_fgpd_latest_prediction
    ON mart.fact_generation_prediction_daily (is_latest_prediction, prediction_date);

CREATE INDEX IF NOT EXISTS ix_fgpd_location_date
    ON mart.fact_generation_prediction_daily (location_sk, prediction_date);

-- ============================================================
-- 2. meta.model_scoring_run_log
--    Scoring run summary log
-- ============================================================
CREATE TABLE IF NOT EXISTS meta.model_scoring_run_log (
    scoring_run_id                 BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    run_id                         BIGINT NOT NULL,

    pipeline_name                  VARCHAR(100) NOT NULL,
    model_name                     VARCHAR(100) NOT NULL,
    model_version                  VARCHAR(50),

    prediction_type                VARCHAR(20) NOT NULL DEFAULT 'day_ahead',
    source_table                   VARCHAR(150) NOT NULL,

    score_date_from                DATE,
    score_date_to                  DATE,

    total_input_rows               INTEGER NOT NULL DEFAULT 0,
    scored_rows                    INTEGER NOT NULL DEFAULT 0,
    output_rows                    INTEGER NOT NULL DEFAULT 0,
    failed_rows                    INTEGER NOT NULL DEFAULT 0,

    status                         VARCHAR(20) NOT NULL DEFAULT 'SUCCESS',
    message                        VARCHAR(1000),

    created_at                     TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT ck_msrl_prediction_type
        CHECK (prediction_type IN ('day_ahead', 'historical_backtest', 'reforecast')),

    CONSTRAINT ck_msrl_total_input_rows
        CHECK (total_input_rows >= 0),

    CONSTRAINT ck_msrl_scored_rows
        CHECK (scored_rows >= 0),

    CONSTRAINT ck_msrl_output_rows
        CHECK (output_rows >= 0),

    CONSTRAINT ck_msrl_failed_rows
        CHECK (failed_rows >= 0),

    CONSTRAINT ck_msrl_status
        CHECK (status IN ('SUCCESS', 'FAILED', 'PARTIAL_SUCCESS'))
);

COMMENT ON TABLE meta.model_scoring_run_log IS
'Summary log for model batch scoring runs.';

COMMENT ON COLUMN meta.model_scoring_run_log.run_id IS
'Foreign-style reference to meta.etl_run_log.run_id for the scoring pipeline run';

CREATE INDEX IF NOT EXISTS ix_msrl_run_id
    ON meta.model_scoring_run_log (run_id);

CREATE INDEX IF NOT EXISTS ix_msrl_model_name_created_at
    ON meta.model_scoring_run_log (model_name, model_version, created_at DESC);

CREATE INDEX IF NOT EXISTS ix_msrl_prediction_type
    ON meta.model_scoring_run_log (prediction_type, created_at DESC);

COMMIT;